    public void limparTabelaTel() {
        DefaultTableModel tabela = (DefaultTableModel) jtTabelaTel.getModel();
        for (int i = tabela.getRowCount() - 1; i >= 0; i--) {
            tabela.removeRow(i);
        }
    }

    public void limparTabDadosCli() {
        DefaultTableModel tabela = (DefaultTableModel) jtTabelaDados.getModel();
        for (int i = tabela.getRowCount() - 1; i >= 0; i--) {
            tabela.removeRow(i);
        }
        jtfPesquisar.setText("");
    }

    public boolean verificaTelRep(DefaultTableModel mp, String ddd, String numero) {
        int quant = mp.getRowCount();
        while (quant != 0) {
            if (mp.getValueAt(mp.getRowCount() - 1, 0).equals(ddd) && mp.getValueAt(mp.getRowCount() - 1, 1).equals(numero)) {
                return true;
            }
            quant--;
        }
        return false;
    }
