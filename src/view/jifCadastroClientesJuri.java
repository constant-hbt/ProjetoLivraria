    public boolean validarCampos() {
        String msg = new String();
        if (jtfNomeFant.getText().trim().length() < 3) {
            msg = "Verifique o nome";
        }
        if (jtfRazaoSocial.getText().trim().length() < 3) {
            msg = "Verifique a razão social";
        }
        if (jtfEmail.getText().trim().length() < 10 || !jtfEmail.getText().trim().contains("@")) {
            msg += "\nVerifique o e-mail";
        }
        if (jftfCnpj.getText().trim().length() != 18) {
            msg += "\nVerifique o CPF";
        }
        if (jftfCep.getText().trim().length() != 10) {
            msg += "\nVerifique o Cep";
        }
        if (jtfRua.getText().trim().length() < 3) {
            msg += "\nVerifique o nome da rua";
        }
        if (jtfBairro.getText().trim().length() < 3) {
            msg += "\nVerifique o nome o bairro";
        }
        if (jtfCidade.getText().trim().length() < 3) {
            msg += "\nVerifique o nome da cidade";
        }
        if (jtfRua.getText().trim().length() < 3) {
            msg += "\nVerifique o nome da rua";
        }
        if (jtfNumero.getText().trim().length() > 5) {
            msg += "\nVerifique o numero do endereço";
        }
        if (jcbEstado.getSelectedIndex() == 0) {
            msg += "\nVerifique o nome da rua";
        }
        if (msg.isEmpty()) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Erros: " + msg, "Validação", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    public boolean preencherObjeto() throws Exception {
        obj.setId(Integer.parseInt(jlNumid.getText()));
        obj.setNome_fant(jtfNomeFant.getText());
        obj.setRazao_social(jtfRazaoSocial.getText());
        obj.setCidade(jtfCidade.getText());
        obj.setRua(jtfRua.getText());
        obj.setBairro(jtfBairro.getText());
        obj.setEmail(jtfEmail.getText());
        obj.setCnpj(jftfCnpj.getText());
        obj.setCep(jftfCep.getText());
        obj.setNumero(jtfNumero.getText());
        obj.setEstado(jcbEstado.getSelectedItem().toString());
        obj.setComplemento(jtfComplemento.getText());
        obj.setObservacoes(jtfObservacoes.getText());
        DefaultTableModel mp = (DefaultTableModel) jtTabelaTel.getModel();
        for (int i = 0; i < mp.getRowCount(); i++) {
            TelefoneModel telefone = new TelefoneModel();
            telefone.setDdd(Integer.parseInt(mp.getValueAt(i, 0).toString()));
            telefone.setNumero(Integer.parseInt(mp.getValueAt(i, 1).toString()));
            telefone.setTipo_tel(mp.getValueAt(i, 2).toString());
            obj.adicionarTelefone(telefone);
        }
        return true;
    }

