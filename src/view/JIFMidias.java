    private void JBSalvar_liActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBSalvar_liActionPerformed
        try{
            if(validarCamposLivro()){
                if(preencherObjetoLivro()){
                    LivroData DAO = new LivroData();
                    if(acao.equals("incluir")){
                        if(DAO.incluir(obj)){
                            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
                            JBCancelar_liActionPerformed(evt);
                        }   
                        else{
                        JOptionPane.showMessageDialog(null, "Erro ao salvar!");
                        }
                    }else{
                        if(DAO.alterar(obj)){
                            JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
                            JBCancelar_liActionPerformed(evt);
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "Erro ao alterar!");
                        }
                    }
                }
            }
        }catch(Exception erro){
            JOptionPane.showMessageDialog(this, "Erro: " + erro.getMessage() + "Salvar" + JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_JBSalvar_liActionPerformed

